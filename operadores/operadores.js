// Operadores aritméticos
{
    // ----------------------------------------------------------------------------
    // Operador de soma
    // ----------------------------------------------------------------------------
    var a = 2;
    var result = a + a;
    console.log(result);
    // ----------------------------------------------------------------------------
    // Operador de subtração
    // ----------------------------------------------------------------------------
    var x = 10;
    var y = 5;
    result = x - y;
    console.log(result);
    // ----------------------------------------------------------------------------
    // Operador de multiplicação
    // ----------------------------------------------------------------------------
    result = x * y * a;
    console.log(result);
    // ----------------------------------------------------------------------------
    // Operador de divisão
    // ----------------------------------------------------------------------------
    result = x / y;
    console.log(result);
    // ----------------------------------------------------------------------------
    // Operador de módulo
    // ----------------------------------------------------------------------------
    result = x % y;
    console.log(result);
    // ----------------------------------------------------------------------------
    // Operadores de incremento e decremento
    // ----------------------------------------------------------------------------
    var i = 1;
    console.log(++i); // 2
    console.log(i++, i); // 2
    console.log(--i); // 2
    console.log(i--, i); // 2
    // ----------------------------------------------------------------------------
    // Concatenação
    // ----------------------------------------------------------------------------
    var hello = 'hello ';
    var world = 'world';
    var texto = hello + world;
    // O resultado será hello world
    console.log(texto);
    var quantidade = 1;
    var objeto = " carro";
    var conteudo = quantidade + objeto;
    // O resultado será '1 carro'
    console.log(conteudo);
}
